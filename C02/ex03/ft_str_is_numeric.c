int	ft_str_is_numeric(char *str)
{
	int	r;
	int	i;

	r = 1;
	i = 0;
	while (str[i])
	{
		if (str[i] < '0' || str[i] > '9')
			r = 0;
		i++;
	}
	return (r);
}
