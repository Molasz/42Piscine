int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	r;

	r = 1;
	i = 0;
	while (str[i])
	{
		if (str[i] < 'a' || str[i] > 'z')
			r = 0;
		i++;
	}
	return (r);
}
